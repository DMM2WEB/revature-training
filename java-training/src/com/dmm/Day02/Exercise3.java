package com.dmm.Day02;

//program to print the multiplication table of a given number n

public class Exercise3 {
    public static void main(String[] args) {
        int number = 5;
        for (int i=1; i<=10; i++) {
            System.out.println(number + " x " + i + " = " +  number * i);
        }
    }
}
